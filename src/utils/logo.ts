export function getMantisLogo(): string {
  const indent = '          '; // Standard centering indent
  let logo = '\n';

  // Build the pixel art mantis
  for (const row of MANTIS_GRID) {
    let line = indent;
    for (const cell of row) {
      if (cell === '') {
        line += '  ';
      } else {
        line += chalk.hex(cell)('██');
      }
    }
    logo += line + '\n';
  }

  logo += '\n';

  // Build the block MANTIS letters using a nice brand green (#84ad35)
  const letterColor = chalk.hex('#84ad35');
  const letters = `
   █▄ ▄█   ▄██▄   ██  ██  ██████  █████   ▄████
   █████  ██  ██  ███ ██    ██     ██    ██    
   █ █ █  ██████  ██████    ██     ██     ▀███▄ 
   █   █  ██  ██  ██ ███    ██     ██        ██ 
   █   █  ██  ██  ██  ██    ██    █████  █████▀
  `;

  logo += letterColor(letters) + '\n';

  // Tagline in sage/teal color (#629489)
  const tagColor = chalk.hex('#629489');
  logo += tagColor('                 powered by mangrove') + '\n\n';

  return logo;
}
